package pojo;

/**
 *
 * @author JDufo
 */
public class Langue {
    private int idLangue;
    private String nom;

    public Langue(int idLangue, String nom) {
        this.idLangue = idLangue;
        this.nom = nom;
    }

    public int getIdLangue() {
        return idLangue;
    }

    public void setIdLangue(int idLangue) {
        this.idLangue = idLangue;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }
    
    
    
}
